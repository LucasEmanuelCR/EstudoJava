package estruturaDeControle;

public class WhileDeterminado {
    public static void main(String[] args) {
        byte contador = 0;
        while (contador <= 10){
            contador++;
            System.out.println(contador);
        }
    }
}
/*Anotações:
    -> 'while' é uma estrutura de repetição.
    -> A estrutura 'while' é execultada enquando o valor da expressão informada seja true
    -> 'while' é um estrutura que possui a quantidade de repetições pré-determinada
 */