package oo.composicao.desafio;

public class Produto {
    // Atributos 'nome' e 'preco' para representar caracteristicas de um produto.
    final String nome;
    double preco;

    // Construtor onde recebe e atribui valores ao atributos 'nome' e 'preco'.
    Produto(String nome, double preco){
        this.nome = nome;
        this.preco = preco;
    }

}
