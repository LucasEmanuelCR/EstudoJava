package estruturaDeControle;

public class For1 {
    public static void main(String[] args) {

        for (byte contador = 0;contador <= 10; contador++){
            System.out.println(contador);
        }
    }
}
/*Anotações:
    -> 'for' é uma estrutura de repetição.
    -> A estrutura do 'for' é uma estrutura focada quando a quantidade de expressão é pré-determinada.
 */